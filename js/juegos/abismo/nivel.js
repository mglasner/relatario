// Spawn del jugador: sobre el suelo al inicio (columna 3, fila 13)
const SPAWN_JUGADOR = { col: 3, fila: 13 };

// Extraer spawns de enemigos y boss del mapa
export function obtenerSpawns() {
    const enemigos = [];
    let boss = null;

    for (let fila = 0; fila < MAPA.length; fila++) {
        for (let col = 0; col < MAPA[fila].length; col++) {
            if (MAPA[fila][col] === T.SPAWN_ENEMIGO) {
                enemigos.push({ col, fila });
                MAPA[fila][col] = T.VACIO; // limpiar spawn del mapa
            } else if (MAPA[fila][col] === T.SPAWN_BOSS) {
                boss = { col, fila };
                MAPA[fila][col] = T.VACIO;
            }
        }
    }

    return { enemigos, boss };
}

export function obtenerSpawnJugador() {
    return { ...SPAWN_JUGADOR };
}

export function obtenerFilas() {
    return MAPA.length;
}

export function obtenerColumnas() {
    return MAPA[0].length;
}

export function obtenerTile(fila, col) {
    if (fila < 0 || fila >= MAPA.length || col < 0 || col >= MAPA[0].length) {
        return T.VACIO;
    }
    return MAPA[fila][col];
}

// Resetear el mapa (restaurar spawns originales para rejugabilidad)
const MAPA_ORIGINAL = MAPA.map(function (fila) {
    return fila.slice();
});

export function resetearMapa() {
    for (let f = 0; f < MAPA.length; f++) {
        for (let c = 0; c < MAPA[f].length; c++) {
            MAPA[f][c] = MAPA_ORIGINAL[f][c];
        }
    }
}
